-- Q23 Give the total weekly sales value with the date and product ID details. Use
-- roll-up to pull the data in hierarchical order.

DECLARE @FirstDate DATE;
SELECT @FirstDate = MIN(Date) FROM fact;
SELECT 
    ProductID,
    DATEADD(DAY, 7 * ((DATEDIFF(DAY, @FirstDate, Date)) / 7), @FirstDate) AS WeekStart,
    DATEADD(DAY, 6, DATEADD(DAY, 7 * ((DATEDIFF(DAY, @FirstDate, Date)) / 7), @FirstDate)) AS WeekEnd,
    SUM(Sales) AS Total_Sales
FROM fact
GROUP BY ROLLUP(
    ProductID, 
    DATEADD(DAY, 7 * ((DATEDIFF(DAY, @FirstDate, Date)) / 7), @FirstDate)
)
ORDER BY WeekStart, ProductId;

