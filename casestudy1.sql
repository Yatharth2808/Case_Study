-- Q25. Create a user-defined function for the product table to fetch a particular
-- product type based upon the user’s preference.

CREATE or Alter FUNCTION fetch_product(@Product_Type VARCHAR(50))
RETURNS TABLE
AS
RETURN
(
    SELECT Product, type, product_type
    FROM Product
    WHERE product_type = @Product_Type
);
GO

Select * from fetch_product('Coffee');

-- Q26. Change the product type from coffee to tea where product ID is 1 and undo it.

-- Start a transaction
BEGIN TRANSACTION;

-- Step 1: Update product_type to 'Tea' for ProductID = 1
UPDATE Product
SET product_type = 'Tea'
WHERE ProductID = 1;

-- Checking the table changes
SELECT ProductID, Product, product_type
FROM Product
WHERE ProductID = 1;

-- Step 2: Undo the change
ROLLBACK TRANSACTION;

-- Checking the product table again
SELECT ProductID, Product, product_type
FROM Product
WHERE ProductID = 1;

